public class CharacterPrinter {
    public static void main(String [] args) {
        int level;
        double health;
        String name;
        level=1;
        health=12.5;
        name = "Mallis";
        System.out.println("Level: "+ level);
        System.out.println("Health: " + health);
        System.out.println("Character name: " + name);
    }
}
